    private void jTextField_searchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField_searchKeyReleased
        ArrayList<KnjigeBean> al=null;
                al=new ArrayList<KnjigeBean>();
       String val=jTextField_search.getText().toString();
        try {
            Connection conn=MySqlConnection();
            String qry="select * from literatura where knjiga like '%"+val+"%'";
            Statement st=conn.createStatement();
            ResultSet rs=st.executeQuery(qry);
            KnjigeBean knjiga;
            while (rs.next()) {
          knjiga =new KnjigeBean(rs.getInt(1),rs.getString("knjiga")
                  , (int) Float.parseFloat(rs.getString(3)),rs.getString(4),rs.getBytes("slika"));                
                al.add(knjiga);
              }
            
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        model.setRowCount(0); // Prazan broj redova u table
        Object[] row=new Object[4];
        for (int i = 0; i < al.size(); i++) {
            row[0]=al.get(i).getId();
            row[1]=al.get(i).getKnjiga();
            row[2]=al.get(i).getOcena();
            row[3]=al.get(i).getZanr();
            model.addRow(row);
        }           
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_jTextField_searchKeyReleased

