    public ArrayList<KnjigeBean> retrieveData(){
        ArrayList<KnjigeBean> al=null;
                al=new ArrayList<KnjigeBean>();
        try {
            Connection conn=MySqlConnection();
            String qry="select * from literatura";
            Statement st=conn.createStatement();
            ResultSet rs=st.executeQuery(qry);
            KnjigeBean book;
            while (rs.next()) {
                book =new KnjigeBean(rs.getInt(1),rs.getString("knjiga"), (int) Float.parseFloat(rs.getString(3)),rs.getString(4),
                        rs.getBytes("slika"));                
                al.add(book);
              }
           
        } catch (Exception e) {
            System.out.println(e);
        }
       return al; 
    }
    //popunjavanje table-a
    public void fillTable(){
        ArrayList<KnjigeBean> al=retrieveData();
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        model.setRowCount(0); // Prazno polje u table-u
        Object[] row=new Object[4];
        for (int i = 0; i < al.size(); i++) {
            row[0]=al.get(i).getId();
            row[1]=al.get(i).getKnjiga();
            row[2]=al.get(i).getOcena();
            row[3]=al.get(i).getZanr();
            model.addRow(row);
        }
    }
    
